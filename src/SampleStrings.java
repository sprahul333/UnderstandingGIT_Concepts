public class SampleStrings {

    public static void main(String[] args) {

        String a="Hello";

        System.out.println(a); //Hello

        System.out.println(a + " World!"); //Hello World!
        System.out.println(a + " " + "World!"); //Hello World!
        System.out.println(a + " " + 123); //Hello 123
    }
}
